import gulp from 'gulp';
import scsslint from 'gulp-scss-lint';
import setting from '../setting/js-hint.js';

/*------------------------------------------------------------------
    task
------------------------------------------------------------------*/
gulp.task('scsslint', function() {

    return gulp
        .src(setting.src)
        .pipe(scsslint())
    ;

});
